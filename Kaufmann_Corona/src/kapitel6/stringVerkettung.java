package kapitel6;

public class stringVerkettung {
    public static void main(String[] args) {
     int a = 5;
     double x = 3.14;
     double c = 5.34;
 
     System.out.println("a = " + a);
     System.out.println("x = " + x);
     System.out.println("Summe = " + (a + x + c));
     System.out.println("3 + 4 = " + (3 + 4));
     System.out.println("3 + 4 = " + 3 + 4);
    }
} 